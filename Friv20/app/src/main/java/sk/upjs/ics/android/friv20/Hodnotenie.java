package sk.upjs.ics.android.friv20;

public class Hodnotenie {
    private String hviezdicky;
    private String popis;
    private String menoPriezvisko;


    public Hodnotenie() {
    }

    public String getMenoPriezvisko() {
        return menoPriezvisko;
    }

    public void setMenoPriezvisko(String menoPriezvisko) {
        this.menoPriezvisko = menoPriezvisko;
    }

    public String getHviezdicky() {
        return hviezdicky;
    }

    public void setHviezdicky(String hviezdicky) {
        this.hviezdicky = hviezdicky;
    }

    public String getPopis() {
        return popis;
    }

    public void setPopis(String popis) {
        this.popis = popis;
    }
}
